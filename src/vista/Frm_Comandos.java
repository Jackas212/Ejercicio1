    private ColaService colaService = new ColaService(tamanio);

    private void cargarTabla() {
        tc.setLista(colaService);
        jTable1.setModel(tc);
        jTable1.updateUI();
    }

    private void guardar() {
        String comando = jTComand.getText().trim();
        SimpleDateFormat dtf = new SimpleDateFormat("yyyy/MM/dd");
        Calendar calendar = Calendar.getInstance();
        Date dateObj = calendar.getTime();
        String formattedDate = dtf.format(dateObj);
        Comandos comand = new Comandos(comando, formattedDate);
        if (historial < tamanio) {
            colaService.Queue(comand);
        } else if (historial >= tamanio) {
            colaService.Dequeue(0);
            colaService.Queue(comand);
        }
        historial++;
        cargarTabla();
    }

